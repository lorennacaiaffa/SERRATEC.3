package br.com.residencia;

public class ExercícioI_Int_para_double {

	public static void main(String[] args) {
		
		int a = 10, b = 5;
		double soma;
		double div;
		
		soma = a + b;
		div = (double) a / b;
		
		System.out.println("A Soma é: "+ soma);
		System.out.println("A divisão é "+ div);
	}

}
